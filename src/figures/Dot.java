package figures;

/**
 *
 * @author evgenii
 */
public class Dot {
    
    private float x;
    
    private float y;
    
    public Dot(float x, float y) {
        this.x = x;
        this.y = y;
    }
    
    public float getX() {
        return x;
    }
    
    public float getY() {
        return y;
    }
}
